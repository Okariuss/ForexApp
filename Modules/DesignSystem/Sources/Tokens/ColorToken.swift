import UIKit

@MainActor
public enum ColorToken {
    public static let systemBackground =
        UIColor.systemBackground

    public static let secondarySystemBackground =
        UIColor.secondarySystemBackground

    public static let primaryLabel =
        UIColor.label

    public static let secondaryLabel =
        UIColor.secondaryLabel

    public static let separator =
        UIColor.separator

    public static let accent =
        UIColor.systemBlue

    public static let negative =
        UIColor.systemRed
}
